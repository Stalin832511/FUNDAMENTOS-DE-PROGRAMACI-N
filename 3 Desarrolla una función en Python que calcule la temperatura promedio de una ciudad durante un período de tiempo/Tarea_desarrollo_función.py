# Función para calcular el promedio de temperaturas para cada ciudad
def calcular_promedio_ciudad(temperaturas):
    """
    Calcula el promedio de temperatura para cada ciudad durante todo el período.

    Parámetros:
    - temperaturas: lista tridimensional donde:
        - La primera dimensión representa las ciudades.
        - La segunda dimensión representa las semanas.
        - La tercera dimensión representa los días de la semana con sus temperaturas.

    Retorna:
    - Una lista con el promedio de temperatura para cada ciudad.
    """
    promedios_ciudades = []  # Lista para almacenar los promedios de cada ciudad

    # Iterar sobre cada ciudad en los datos de temperaturas
    for i, ciudad in enumerate(temperaturas):
        suma_total = 0  # Variable para acumular la suma de temperaturas de la ciudad
        dias_totales = 0  # Contador para el número total de días considerados

        # Iterar sobre cada semana de la ciudad
        for semana in ciudad:
            # Iterar sobre cada día de la semana
            for dia in semana:
                suma_total += dia['temp']  # Sumar la temperatura del día
                dias_totales += 1  # Incrementar el contador de días

        # Calcular el promedio de la ciudad
        promedio_ciudad = suma_total / dias_totales
        promedios_ciudades.append(promedio_ciudad)  # Agregar el promedio a la lista

    return promedios_ciudades  # Devolver la lista de promedios por ciudad


